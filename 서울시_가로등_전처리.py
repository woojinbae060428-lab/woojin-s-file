# CP949 인코딩으로 읽기
df_raw = pd.read_csv(filename, encoding='cp949')

print(f'📊 원본 데이터 shape: {df_raw.shape}')
print()
print(df_raw.head(10))
print()
print('▶ 컬럼 타입:')
