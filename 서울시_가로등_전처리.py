"""## Step 2. 데이터 품질 점검"""

print('=' * 50)
print('📋 기본 통계')
print('=' * 50)
print(df_raw.describe())

print()
print('=' * 50)
print('🔍 결측치 현황')
print('=' * 50)
missing = df_raw.isnull().sum()
print(missing)
print(f'→ 결측치 있는 컬럼: {(missing > 0).sum()}개')

print()
print('=' * 50)
print('🔁 중복 행')
print('=' * 50)
dup = df_raw.duplicated().sum()
print(f'→ 중복 행: {dup}건')

print()
print('=' * 50)
print('🌏 좌표 범위')
print('=' * 50)
print(f'위도 범위: {df_raw["위도"].min():.6f} ~ {df_raw["위도"].max():.6f}  (서울 정상범위: 37.4 ~ 37.7)')
print(f'경도 범위: {df_raw["경도"].min():.6f} ~ {df_raw["경도"].max():.6f}  (서울 정상범위: 126.7 ~ 127.2)')

# ─────────────────────────────────────────────
# 좌표 이상치 탐지
# ─────────────────────────────────────────────
SEOUL_LAT_MIN, SEOUL_LAT_MAX = 37.4, 37.7
SEOUL_LON_MIN, SEOUL_LON_MAX = 126.7, 127.2

mask_outlier = (
    (df_raw['위도'] < SEOUL_LAT_MIN) | (df_raw['위도'] > SEOUL_LAT_MAX) |
    (df_raw['경도'] < SEOUL_LON_MIN) | (df_raw['경도'] > SEOUL_LON_MAX)
)

df_outliers = df_raw[mask_outlier].copy()

print(f'⚠️  좌표 이상치: {len(df_outliers)}건')
print()
print(df_outliers)

# 이상치 관리번호 패턴 확인
print('이상치 관리번호 목록:')
print(df_outliers['관리번호'].unique())

fig, axes = plt.subplots(1, 2, figsize=(14, 5))

axes[0].hist(df_raw['위도'], bins=100, color='steelblue', edgecolor='white')
axes[0].axvline(SEOUL_LAT_MIN, color='red', linestyle='--', label='서울 경계')
axes[0].axvline(SEOUL_LAT_MAX, color='red', linestyle='--')
axes[0].set_title('위도 분포')
axes[0].set_xlabel('위도')
axes[0].legend()

axes[1].hist(df_raw[df_raw['경도'] < 200]['경도'], bins=100, color='coral', edgecolor='white')
axes[1].axvline(SEOUL_LON_MIN, color='red', linestyle='--', label='서울 경계')
axes[1].axvline(SEOUL_LON_MAX, color='red', linestyle='--')
axes[1].set_title('경도 분포 (이상치 제외)')
axes[1].set_xlabel('경도')
axes[1].legend()

plt.tight_layout()
plt.show()

"""## Step 3. 전처리"""

df = df_raw.copy()

# ── 3-1. 중복 제거 ──────────────────────────
before = len(df)
df = df.drop_duplicates()
print(f'[중복 제거] {before} → {len(df)}행 ({before - len(df)}건 제거)')

# ── 3-2. 결측치 제거 ────────────────────────
before = len(df)
df = df.dropna(subset=['위도', '경도', '관리번호'])
print(f'[결측치 제거] {before} → {len(df)}행 ({before - len(df)}건 제거)')

# ── 3-3. 좌표 이상치 처리 ───────────────────
# 이상치는 별도 저장 후 정상 데이터만 유지
mask_valid = (
    df['위도'].between(SEOUL_LAT_MIN, SEOUL_LAT_MAX) &
    df['경도'].between(SEOUL_LON_MIN, SEOUL_LON_MAX)
)
before = len(df)
df_invalid = df[~mask_valid].copy()   # 이상치 별도 보관
df = df[mask_valid].copy()            # 정상 데이터만
print(f'[좌표 이상치 제거] {before} → {len(df)}행 ({before - len(df)}건 제거)')

# ── 3-4. 관리번호에서 구역명/번호 분리 ──────
# 형식: '가락지하차도-01' → 구역명='가락지하차도', 순번='01'
split = df['관리번호'].str.rsplit('-', n=1, expand=True)
df['구역명'] = split[0].str.strip()
df['순번'] = split[1].str.strip().str.zfill(3)  # 3자리 0패딩

# ── 3-5. 컬럼 순서 정리 ─────────────────────
df = df[['관리번호', '구역명', '순번', '위도', '경도']].reset_index(drop=True)

print()
print('✅ 전처리 완료!')
print(f'최종 행 수: {len(df):,}건')
print()
print(df.head(10))

# 전처리 결과 요약
print('=' * 45)
print('📊 전처리 전후 비교')
print('=' * 45)
print(f'  원본 데이터 : {len(df_raw):>6,}건')
print(f'  이상치 제거 : {len(df_invalid):>6,}건')
print(f'  최종 데이터 : {len(df):>6,}건')
print()
print(f'  구역명 종류 : {df["구역명"].nunique():,}개')
print()
print('▶ 최종 데이터 컬럼 타입:')
print(df.dtypes)
print()
print('▶ 최종 데이터 기술통계:')
print(df[['위도','경도']].describe().round(6))

"""## Step 4. 탐색적 데이터 분석 (EDA)"""

